// Java program to calculate the area of a rectangle
// using the formula: Area = length * width.

public class Area_rec {
    public static void main(String[] args) {
        double length = 10.5; // Example length
        double width = 5.0;  // Example width

        double area = length * width; // Calculate the area

        System.out.println("The length of the rectangle is: " + length);
        System.out.println("The width of the rectangle is: " + width);
        System.out.println("The area of the rectangle is: " + area);
    }
}